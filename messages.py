def get_text(lang, key, **kwargs):
    texts_to_use = MESSAGES.get(lang, MESSAGES['ru'])
    message_template = texts_to_use.get(key, '')

    if not message_template:
        fallback_lang = 'en' if lang == 'ru' else 'ru'
        message_template = MESSAGES.get(fallback_lang, {}).get(key, '')
        
    for k, v in kwargs.items():
        if k == 'valute' and v == 'ton':
            v = 'TON'
        elif k == 'valute' and v == 'eth':
            v = 'ETH'
        # elif k == 'valute' and v == 'star': # –£–¥–∞–ª–µ–Ω–æ –ø–æ –∑–∞–ø—Ä–æ—Å—É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        #     v = '‚≠êÔ∏è Stars'
            
        message_template = message_template.replace('{' + k + '}', str(v))
        
    return message_template